<?php
    /* Existem 2 jeitos de usar os operadores de atribuição:
        Pegando o numero de uma variavel e outro: numero [operador]= numero;
        Ou somando 2 números colocados por você. Obs: Também é possível: $nova_var = $num [operador] $num_2; */
    // Adição +
    $numero = 2;
    $numero += 3;
    $outro_numero = 2 + 5;

    // Subtração -
    $numero -= 2;
    $outro_numero = 1 + 10;
    
    // Vezes *
    $numero *= 8;
    $outro_numero = 10 * 8;

    // Divisão /
    $numero /= 2;
    $outro_numero = 10 / 5;

    // Módulo %
    $numero %= 3;
    $ outro_numero = 10 % 2;    
?>
